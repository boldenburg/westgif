var user_settings_file = 'user_settings.json'
var user_settings = {};
jsonfile.readFile(user_settings_file, function(err, obj) {
    if(!err) {
        user_settings = obj;
    }
})

var bot_settings = {}
var admin_settings = {}
var config_file = 'config.json';
obj = jsonfile.readFileSync(config_file);
bot_settings = obj['bot_settings'];
admin_settings = obj['admin_settings'];

var admin_bot = new Bot(admin_settings, false);
var bot = new Bot(bot_settings, true);


console.log("bot starting");


// helper methods
function should_process_this_message(message) {
    return message.type == "message"
        && !message.hidden
        && !message.bot_id
        && !message.edited
        && message.user == "U2G6R1PTP" //reefdog
        // && message.user == "U087HSZR9" //baron
        && message.text.startsWith("/gif");
}

function get_random_west_wing_gif() {
    return west_wing_gifs[Math.floor(Math.random() * west_wing_gifs.length)];
}

function delete_message(message) {
    admin_bot._api('chat.delete', {
        "token": admin_settings.token,
        "ts": message.ts,
        "channel": message.channel,
        "as_user": true
    });
}


// main
bot.on('message', function(message) {
    if (should_process_this_message(message)) {
        var params = {
            icon_url: 'https://slack-files2.s3-us-west-2.amazonaws.com/avatars/2015-12-05/16028820502_e357812b4c9cfd3f9848_36.jpg'
        }

        // delete next rightgif message in this channel
        if (delete_next_rightgif_message[message.channel]) {
            delete_next_rightgif_message[message.channel]++;
        } else {
            delete_next_rightgif_message[message.channel] = 1;
        }

        // insert random west wing gif
        var text = get_random_west_wing_gif();
        var channel = message.channel;
        bot.postMessage(
            message.channel,
            text,
            params
        );
    } else if (delete_next_rightgif_message[message.channel] > 0 && message.bot_id && message.bot_id == "B0JSSTL1E") {
        console.log("deleting rightgif message");
        delete_message(message);
        delete_next_rightgif_message[message.channel]--;
    }
});
